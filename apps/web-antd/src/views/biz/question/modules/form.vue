<script lang="ts" setup>
import type { QuestionApi } from '#/api/biz/question';

import { computed, ref } from 'vue';

import { message } from 'ant-design-vue';
import { useVbenModal } from '@vben/common-ui';

import { $t } from '#/locales';
import { useVbenForm } from '#/adapter/form';
import {
  createQuestion,
  getQuestion,
  updateQuestion,
} from '#/api/biz/question';

import { useFormSchema } from '../data';

const emit = defineEmits(['success']);
const formData = ref<QuestionApi.Question>();

const getTitle = computed(() => {
  return formData.value?.id
    ? $t('ui.actionTitle.edit', ['题目'])
    : $t('ui.actionTitle.create', ['题目']);
});

const [Form, formApi] = useVbenForm({
  commonConfig: {
    componentProps: {
      class: 'w-full',
    },
    formItemClass: 'col-span-2',
    labelWidth: 80,
  },
  layout: 'horizontal',
  schema: useFormSchema(),
  showDefaultActions: false,
});

const [Modal, modalApi] = useVbenModal({
  fullscreen: true,
  async onConfirm() {
    const { valid } = await formApi.validate();
    if (!valid) {
      return;
    }
    modalApi.lock();
    // 提交表单
    const data = (await formApi.getValues()) as QuestionApi.Question;
    try {
      await (formData.value?.id ? updateQuestion(data) : createQuestion(data));
      // 关闭并提示
      await modalApi.close();
      emit('success');
      message.success($t('ui.actionMessage.operationSuccess'));
    } finally {
      modalApi.unlock();
    }
  },
  async onOpenChange(isOpen: boolean) {
    if (!isOpen) {
      formData.value = undefined;
      return;
    }

    // 加载数据
    let data = modalApi.getData<QuestionApi.Question>();
    if (!data) {
      return;
    }
    if (data.id) {
      modalApi.lock();
      try {
        data = await getQuestion(data.id);
      } finally {
        modalApi.unlock();
      }
    }
    // 设置到 values
    formData.value = data;
    await formApi.setValues(formData.value);
  },
});
</script>

<template>
  <Modal :title="getTitle">
    <Form class="mx-4" />
  </Modal>
</template>